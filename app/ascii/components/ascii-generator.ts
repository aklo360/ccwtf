const FONTS: { [key: string]: FontMap } = {
  standard: STANDARD_FONT,
  small: SMALL_FONT,
  banner: BANNER_FONT,
  block: BLOCK_FONT,
  bubble: BUBBLE_FONT,
  digital: DIGITAL_FONT,
};

export function generateASCII(text: string, fontStyle: string = 'standard'): string {
  const font = FONTS[fontStyle] || FONTS.standard;
  const chars = text.toUpperCase().split('');

  // Get the height of characters in this font
  const height = font['A']?.length || 6;

  // Build each line
  const lines: string[] = Array(height).fill('');

  for (const char of chars) {
    const charArt = font[char] || font[' '];

    for (let i = 0; i < height; i++) {
      lines[i] += (charArt[i] || ' '.repeat(charArt[0]?.length || 3)) + ' ';
    }
  }

  return lines.join('\n');
}
